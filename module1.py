#What is Python?
#ANS---->
'''Python is an interpreted,   object-oriented, high-level 
programming language with dynamic semantics  developed  by
Guido van Rossum. It    was originally    released in 1991. 
Designed to be easy as    well as fun,    the name "Python" 
is a nod to the British comedy    group    Monty    Python. 
Python has a reputation as a   beginner-friendly   language, 
replacing Java as the most widely used introductory language 
because it handles much of the complexity for the user, allowing 
beginners to focus on fully grasping programming concepts rather 
than minute details.'''