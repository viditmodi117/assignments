# What are negative indexes and why are they used?
#ANS.---->
''' Negative Indexing is used to in Python to begin slicing from the end of the string i.e. the last. 
Slicing in Python gets a sub-string from a string. The slicing range is set as parameters i.e. start, 
stop and step.You can use negative indexing as your advantage when you want to pick values from the end 
(right side) of an iterable.'''