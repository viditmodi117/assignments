# What is the purpose continue statement in python?
#ANS.--->
'''Python Continue statement is a loop control statement that forces to execute the next 
iteration of the loop while skipping the rest of the code inside the loop for the current
iteration only, i.e. when the continue statement is executed in the loop, the code inside 
the loop following the continue statement will beskipped for the current iteration and the 
next iteration of the loop will begin.'''
